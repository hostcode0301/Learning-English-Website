	<h2>*Wh-question:</h2>
    <h4>Một số câu hỏi thường được bắt đầu với từ để hỏi (question word). Các từ để hỏi thường là Who (Ai), What (Cái gì), Which (Cái nào), Whose (Của ai), Where (Ở đâu), When (Khi nào), Why (Tại sao) và How (Thế nào). Loại câu hỏi này còn được gọi là câu hỏi trực tiếp (direct questions).</h4>
<h4>- Cấu trúc thông thường của loại câu hỏi Wh- questions:</h4>
<h4>Question word + auxiliary + subject + main verb + rest of question?</h4>
<h4>(Từ để hỏi + trợ động từ + chủ ngữ + động từ chính + phần còn lại)</h4>
<h4>Hoặc: Question word + be + subject + rest of question?</h4>
<h4>(Từ để hỏi + động từ to be chia theo ngữ cảnh + chủ ngữ + phần còn lại)</h4>
<h4>Hoặc: Question word + main verb + rest of question?</h4>
<h4>(Từ để hỏi + động từ chính + phần còn lại) *Chú ý trong trường hợp này, nếu ở thì hiện tại đơn, động từ luôn được chia với ngôi 3 số ít, VD: who volunteers?</h4>
<h4>- Khi "be" là động từ chính trong câu hỏi, nó được đặt trước chủ ngữ.</h4>
<h4>Ví dụ: Where is John? Who are you?...</h4>
<h4>- Nếu có nhiều hơn một trợ động từ, ta chỉ đặt trợ động từ đầu tiên trước chủ ngữ.</h4>
<h4>Ví dụ: How long has the bridge been built?</h4>
<h4>Ngoài ra còn có 1 số từ để hỏi khác:How (Thế nào?), How much/ many (Bao nhiêu?), How often (Bao lần?), How long (Bao lâu?), How far (Bao xa?)</h4>
	<h2>*Gerund and to + infiitive:</h2>	
    <h4>I. INFINITIVE</h4>
<h4>1. To-infinitive / Infinitive with to: Động từ nguyên mẫu có “to”</h4>
<h4>* Động từ nguyên mẫu có to được dùng làm:</h4>
<h4>- Chủ ngữ của câu:          </h4>
<h4>Ex: To become a famous singer is her dream.</h4>
<h4>- Bổ ngữ cho chủ ngữ:     </h4>
<h4>Ex: What I like is to swim in the sea and then to lie on the warm sand.</h4>
<h4>- Tân ngữ của động từ     </h4>
<h4>Ex: It was late, so we decided to take a taxi home.</h4>
<h4>- Tân ngữ của tính từ       </h4>
<h4>Ex: I’m pleased to see you.</h4>
<h4>* V + to-inf         </h4>
<h4>- Sau các động từ: Agree , appear, afford, ask, demand, expect, hesitate, intend, invite, want, wish, hope, promise, decide, tell, refuse, learn, fail (thất bại), plan, manage, pretend (giả vờ), remind, persuade, encourage, force, order, urge (thúc giục), seem, tend, threaten,…</h4>
<h4>- Trong các cấu trúc:</h4>
   <h4>+ It takes / took + O + thời gian + to-inf                    </h4>
   <h4>+ chỉ mục đích</h4>
   <h4>+ bổ ngữ cho danh từ hoặc đại từ: S + V + Noun / pronoun + to-inf</h4>
           <h4>I have some letters to write.</h4>
           <h4>Is there anything to eat?</h4>
   <h4>+ It + be + adj + to-inf: thật … để ..</h4>
<h4>Ex: It is interesting to study English</h4>
   <h4>+ S + be + adj + to-inf</h4>
<h4>Ex: I’m happy to receive your latter.</h4>
   <h4>+ S + V + too + adj / adv + to-inf</h4>
   <h4>+ S + V + adj / adv + enough + to-inf</h4>
   <h4>+ S + find / think / believe + it + adj + to-inf</h4>
<h4>Ex: I find it difficult to learn English vocabulary.</h4>
<h4>- Sau các từ nghi vấn: what, who, which, when, where, how ,… (nhưng thường không dùng sau why)</h4>
<h4>Ex: I don’t know what to say.</h4>
<h4>* Adj + to-inf:</h4>
<h4>- Hình thức nguyên mẫu có to thường được dùng sau một số tính từ diễn tả phản ứng hoặc cảm xúc của con người</h4>
<h4>(un)able                delighted                 proud                         glad</h4>
<h4>ashamed              afraid                      eager                         surprised</h4>
<h4>anxious                pleased                   amused                      easy</h4>
<h4>annoyed               happy                     ready</h4>
<h4>* Note:</h4>
<h4>- allow / permit/ advise / recommend + O + to-inf                 </h4>
<h4>Ex: She allowed me to use her pen.</h4>
<h4>- allow / permit / advise / recommend + V-ing                       </h4>
<h4>Ex: She didn’t allow smoking in her room.</h4>
<h4>- be allowed / permitted + to-inf</h4>
<h4>Ex: I was allowed to go out with my friends last night.</h4>
<h4>2. Bare infinitive / Infinitive without to (V1): Động từ nguyên mẫu không “to”</h4>
<h4>Động từ nguyên mẫu không to được dùng:</h4>
<h4>- Sau động từ khiếm khuyết: can, will, shall, could, would,…</h4>
<h4>- Sau các động từ: let, make, would rather, had better</h4>
<h4>Ex: They made him repeat the whole story.</h4>
<h4>- Các động từ chỉ tri giác: hear, sound, smell, taste, feel, watch, notice, see, listen, find .. <h4>+ O + V1 (chỉ sự hoàn tất của hành động – nghe hoặc thấy toàn bộ sự việc diễn ra)</h4>
<h4>Ex: I saw her get off the bus.</h4>
<h4>- Help + to-inf / V1 / with Noun</h4>
<h4>Ex: He usually helps his sister to do her homework.</h4>
      <h4> He usually helps his sister do her homework.</h4>
       <h4>He usually helps his sister with her homework.</h4>
<h4>II. GERUND (V-ing): Danh động từ</h4>
<h4>* Danh động từ có thể được dùng làm:</h4>
<h4>- Chủ từ của câu:                  Swimming is my favourite sport.</h4>
<h4>- Bổ ngữ của động từ:          My hobby is collecting stamps.</h4>
<h4>- Tân ngữ của động từ:         I enjoy traveling. </h4>
<h4>* V + V-ing</h4>
<h4>- Sau các động từ: enjoy, avoid, admit, appreciate (đánh giá cao), mind (quan tâm, ngại), finish, practice, suggest, postpone (hoãn lại), consider (xem xét), hate, admit (thừa nhận), like, love, deny (phủ nhận), detest (ghét), keep (tiếp tục), miss (bỏ lỡ), imagine (tưởng tượng), mention, risk, delay (trì hoãn), ….</h4>
<h4>- Sau các cụm động từ: cant’ help (không thể không), can’t bear / can’t stand (không thể chịu được), be used to, get used to, look forward to, it’s no use / it’s no good (không có ích lợi gì), be busy, be worth (đáng giá)</h4>
<h4>- Sau giới từ: in, on, at, from, to, about …</h4>
<h4>- Sau các liên từ: after, before, when, while, since,…</h4>
<h4>Ex: You should lock the door when leaving your room.</h4>
<h4>- S + spend / waste + time / money + V-ing</h4>
<h4>Ex: I spent thirty minutes doing this exercise.</h4>
<h4>III. INFINITIVE OR GERUND</h4>
<h4>1. Không thay đổi nghĩa:</h4>
<h4>- begin / start / continue/ like / love + to-inf / V-ing</h4>
<h4>Ex: It started to rain / raining.</h4>
<h4>2. Thay đổi nghĩa:</h4>
<h4>+ remember / forget / regret + V-ing: nhớ / quên/ nuối tiếc việc đã xảy ra rồi (trong quá khứ)</h4>
<h4>+ remember / forget / regret + to-inf: nhớ / quên/ nuối tiếc việc chưa, sắp xảy ra (trong tương lai)</h4>
<h4>Ex: Don’t forget to turn off the light when you go to bed.</h4>
      <h4>I remember meetingyou some where but I can’t know your name.</h4>
      <h4>Remember to send her some flowers because today is her birthday.</h4>
<h4>+ stop + V-ing: dừng hẳn việc gì</h4>
<h4>+ stop + to-inf: dừng ….. để …</h4>
<h4>Ex: He stopped smoking because it is harmful for his health.</h4>
      <h4>On the way home, I stopped at the post office to buy a newspaper.</h4>
<h4>+ try + V-ing: thử</h4>
<h4>+ try + to-inf: cố gắng</h4>
<h4>+ need + V-ing = need + to be + V3: cần được (bị động)</h4>
<h4>+ need + to-inf: cần (chủ động)</h4>
<h4>Ex: I need to wash my car.</h4>
     <h4> My car is very dirty. It needs washing / to be washed.</h4>
        <h4> + Cấu trúc nhờ vả:</h4>
<h4>S + have + O người + V1 + O vật ...</h4>
<h4>S + have + O vật + V3 + (by + O người) ...</h4>
<h4>S + get + O người + to-inf + O vật</h4>
<h4>S + get + O vật + V3 + (by + O người)</h4>
