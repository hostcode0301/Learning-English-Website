    <h2>*Thì quá khứ đơn(The past simple):</h2>
    	<h4>-Động từ to be:</h4>
        <h4>+ Khẳng định: S + was/were + ....</h4>
        <h4>+ Phủ định:   S + was/were + not + ....</h4>
        <h4>+ Câu hỏi:    Was/Were + S + .... ?</h4>
	<h4>- Động từ thường:</h4>
    	<h4>+ Khẳng định: S + V2/ed + ....</h4>
        <h4>+ Phủ định:   S + did + not + V0 + ....</h4>
        <h4>+ Câu hỏi:    Did + S + V0 + .... ?</h4>
    <h4>- Cách dùng:</h4>
    	<h4>+ Diễn đạt một hành động xảy ra một, một vài lần hoặc chưa bao giờ xảy ra trong quá khứ</h4>
        <h4>+ Diễn đạt các hành động xảy ra liên tiếp trong quá khứ</h4>
        <h4>+ Diễn đạt một hành động xen vào một hành động đang diễn ra trong quá khứ</h4>
    <h4>- Từ nhận diện:</h4>
    	<h4>Yesterday, ago, last, first, later, then, after  that,  finally, at last…</h4>
