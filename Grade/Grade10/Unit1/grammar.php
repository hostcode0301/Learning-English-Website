     <h2>Trạng từ chỉ tần suất(Adverbs of frequency):</h2>
     <h4>- Cách dùng:</h4>
     <h4>Trạng từ chỉ tần suất diễn tả mức độ thường xuyên của một hành động (thường thường, luôn luôn, ít khi…).</h4>
     <h4>Trạng từ chỉ tần suất được dùng để trả lời câu hỏi với: “How often?” (Có…thường?).</h4>
     <h4>- Một số trạng từ thường dùng: </h4>
     <h4>+ Always :	Luôn luôn</h4>
	 <h4>+ Usually	: Thường xuyên</h4>
	 <h4>+ Nomally, Generally : Thông thường, thường lệ</h4>
	<h4>+ Often, Frequently : Thường thường</h4>
	<h4>+ Sometimes : Đôi khi, đôi lúc</h4>
	<h4>+ Occasionally : Thỉnh thoảng</h4>
	<h4>+ Hardly ever :	Hầu như không bao giờ</h4>
	<h4>+ Rarely : Hiếm khi</h4>
	<h4>+ Never : Không bao giờ</h4>
    <h4>- Vị trí trạng từ chỉ tần suất:</h4>
    <h4>+ Đứng sau động từ to be</h4>
    <h4>+ Đứng trước động từ thường</h4>
    <h4>+ Đứng giữa trợ động từ và động từ chính</h4>
    <h4>+ Các trạng từ chỉ tần suất (trừ always, hardly, ever và never) còn có thể xuất hiện ở đầu câu và cuối câu. Vị trí đứng đầu câu được dùng với mục đích nhấn mạnh.</h4>
