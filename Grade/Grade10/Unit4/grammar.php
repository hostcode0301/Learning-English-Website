    <h3>* THE + ADJECTIVE</h3> 
    <h4>- The + adjective dùng để chỉ một nhóm người trong xã hội</h4>

<h4>Ex: - the blind            người mù                - the poor                  người nghèo</h4>
<h4>      - the deaf            người điếc              - the rich                   người giàu</h4>
<h4>      - the old               người già                - the sick                  người bệnh</h4>
<h4>      - the young          người trẻ tuổi          - the handicapped       người tàn tật</h4>
<h4>      - the homeless     người vô gia cư       - the unemployed       người thất nghiệp</h4>
<h4>Note:</h4>
<h4>Khi the + adjective làm chủ ngữ trong câu thì động từ được chia ở hình thức số nhiều.</h4>
<h4>Ex: The rich aren’t always happy in their life.</h4>
<h3>* Used to (từng, đã từng)</h3>
<h4>Dùng để chỉ một thói quen hay tình trạng xảy ra trong quá khứ, không còn xảy ra ở hiện tại. Nó thường chỉ được sử dụng ở quá khứ đơn.</h4>

<h4>Cấu trúc: Used to + Verb</h4>

<h4>Example: I used to live in London. (Tôi đã từng sống ở London)</h4>

<h4>I didn’t used to stay up late when I was a child (Tôi không từng thức khuya khi tôi còn nhỏ)</h4>

<h4>Dạng câu hỏi của Used to: Did(n’t) + subject + use to?</h4>
