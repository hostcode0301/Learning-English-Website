<h3>* Which as a connector</h3>
<h4>Which là một đại từ quan hệ, được dùng để thay thế cho vật, con vật hoặc một mệnh đề.</h4>
<h4>Ex: a/ The book is mine. It is on the table.</h4>
    <h4>  → The book which is on the table is mine.</h4>
     <h4> b/ This is the letter. I received this letter two days ago.</h4>
     <h4> → This is the letter which I received two days ago.</h4>
     <h4> c/ She didn’t come to the party. That made me sad.</h4>
     <h4> → She didn’t come to the party, which made me sad.</h4>
<h4>Note:</h4>
<h4>Khi which dùng thay cho ý của cả mênh đề phía trước thì nó thường thay cho các từ This, That, It và có sử dụng dấu phẩy trước which.</h4>
<h4>Ex: My phone is out of order. It’s a real nuisance.</h4>
<h4>   →My phone is out of order, which is a real nuisance.</h4>
