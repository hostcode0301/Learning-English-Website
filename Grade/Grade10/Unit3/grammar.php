    <h2>*Thì quá khứ hoàn thành(The past perfect):</h2>
       	<h4>+ Khẳng định: S + had + V3/ed + ....</h4>
        <h4>+ Phủ định:   S + had + not + V3/ed + ....</h4>
        <h4>+ Câu hỏi:    Had + S + V3/ed + .... ?</h4>
    <h4>- Cách dùng:</h4>
    	<h4>+ Khi có một hành động xảy ra và hoàn thành trước một hành động khác trong quá khứ</h4>
        <h4>+ Hành động xảy ra một khoảng thời gian trong quá khứ, trước một mốc thời gian khác</h4>
        <h4>+ Hành động xảy ra như là điều kiện tiên quyết cho hành động khác</h4>
    <h4>- Từ nhận diện:</h4>
    	<h4>until then, by the time, prior to that time, before, after, for, as soon as, by, before, after, when by, by the time, by the end of + time in the past … </h4>
    <h2>* So sánh thì Quá Khứ Hoàn Thành và thì Quá Khứ Đơn:</h2>
<h4>Chắc chắn có một số bạn đang cảm thấy bối rối khi phân biệt thì quá khứ hoàn thành và thì quá khứ đơn vì cả hai thì này đều xảy ra và kết thúc trong quá khứ nhưng vội cảm thấy chán nản. Hãy nhìn những ví dụ sau đây, nó sẽ giúp bạn phân biệt được hai thì này:</h4>

<h4>Ex 1: Was Tom at the party when you arrived? (Tom có ở buổi tiệc khi bạn tới không?)</h4>
<h4>No, he had already gone home. (Không, anh ấy đã về nhà rồi)</h4>

<h4>Was Tom there when you arrived? (Lúc bạn đến Tom còn ở đó không?)</h4>
<h4>Yes, but he went home soon afterward. (Có, nhưng mà ngay sau đó anh ta về nhà)</h4>

<h4>Ex 2: Ann wasn’t at home when I phonned. She was in London</h4>
<h4>(Ann không có ở nhà khi tôi gọi. Cô ấy đang ở London)</h4>

<h4>Ann had just got home when I phonned. She had been in London.</h4>
<h4>(Ann chỉ vừa về nhà khi tôi gọi. Cô ấy đã đi London) è Câu này có nghĩa là khi tôi gọi thì Ann mới về nhà, trước đó cô ta ở London.</h4>
