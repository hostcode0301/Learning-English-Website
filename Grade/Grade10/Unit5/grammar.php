<h3>* ĐẠI TỪ QUAN HỆ (relative pronouns)</h3><h4> Có 3 chức năng ngữ pháp chính trong một câu:</h4>
<h4>- Thay cho một danh từ ngay trước nó, làm một nhiệm vụ trong mệnh đề (clause) theo sau, liên kết mệnh đề với nhau.</h4>
<h4>- Đại từ quan hệ có hình thức không thay đổi dù thay cho một danh từ số ít hay số nhiều.</h4>
<h4>- Động từ theo sau thay đổi tùy theo tiền tiến từ của đại từ quan hệ. Mệnh đề có chứa đại từ quan hệ được gọi là mệnh đề quan hệ (relative clause) hay mệnh đề tính ngữ (adjective clause). Danh từ được đại từ quan hệ thay thế gọi là tiền tiến từ (antecedent) của nó.</h4>
<h4>Có 5 đại từ quan hệ chính với chức năng ngữ pháp như trong bảng kê sau:</h4>
<h4>Đại từ quan hệ  Thay thế cho loại danh từ                      Nhiệm vụ trong câu</h4>
<h4>Who                             chỉ người                                              chủ từ</h4>
<h4>Whom                           chỉ người                                              túc từ</h4>
<h4>Which                           chỉ vật                                                  chủ từ hay túc từ</h4>
<h4>That                            chỉ người hay chỉ vật                             chủ từ hay túc từ</h4>
<h4>Whose                        (chỉ người)                                             chỉ quyền sở hữu</h4>
<h4>Ví dụ:</h4>
<h4>Do you know the boy who has broken that chair?</h4>
<h4>The man whom you want to meet is not here.</h4>
<h4>The dog which was lost has been found.</h4>
<h4>Relative Pronoun THAT</h4>
<h4>THAT bắt buộc dùng trong những trường hợp sau:</h4>
<h4>1.         Sau những tính từ ở dạng so sánh cực cấp (superlative).</h4>
<h4>Yesterday was one of the coldest days that I have ever known.</h4>
<h4>2.         Sau những cách nói mở đầu bằng ‘It is/was...’</h4>
<h4>It is the teacher that is important, not the kind of school he teaches in.</h4>
<h4>3.         Sau những tiền tiến từ (antecedent) vừa là người, vừa là vật.</h4>
<h4>He talked brilliantly of the men and the books that interested him.</h4>
<h4>Relative Pronoun WHOSE</h4>
<h4>WHOSE thay cho một danh từ chỉ người đứng trước, chỉ quyền sở hữu đối với danh từ theo sau nó. Giữa WHOSE và danh từ theo sau không có mạo từ (article). Ms Tuyến English sẽ chỉ ra cho bạn: đôi khi WHOSE cũng được dùng thay cho danh từ chỉ vật ở trước. Trong các trường hợp khác người ta dùng OF WHICH.</h4>
<h4>- The man whose car was stolen yesterday is my uncle.</h4>
<h4>- He came in a car the windows of which was broken.</h4>
<h4>Tính chất DEFINING và NON-DEFINING</h4>
<h4>Đại từ quan hệ (relative pronouns) có thể được dùng trong những mệnh đề xác định (defining clause) hay những mệnh đề không xác định (non-defining clause). Ở một số tài liệu khác người ta còn gọi là mệnh đề hạn chế (restrictive clause) hay mệnh đề không hạn chế (non-restrictive clause).</h4>
<h4>Mệnh đề xác định (defining clause) là những mệnh đề giúp làm rõ nghĩa tiền tiến từ.</h4>
<h4>Không có mệnh đề này ta không hiểu rõ nghĩa mệnh đề còn lại.</h4>
<h4>- The man whom you met yesterday is a dentist.</h4>
<h4>Không có mệnh đề whom you met yesterday ta không rõ the man đó là ai.</h4>
<h4>Mệnh đề không xác định (non-defining clause) là mệnh đề không làm rõ nghĩa tiền tiến từ. Không có nó mệnh đề còn lại vẫn rõ nghĩa.</h4>
<h4>- My father, whom you met yesterday, is a dentist.</h4>
<h4>Không có mệnh đề whom you met yesterday người ta vẫn hiểu rõ mệnh đề còn lại.</h4>
<h4>Nhờ có tính chất xác định và không xác định này mà ta có thể hiểu rõ nghĩa các câu sau:</h4>
<h4>(a)        All the books, which had pictures in them, were sent to Daisy.</h4>
<h4>(b)        All the books which had pictures in them were sent to Daisy.</h4>
<h4>Ở câu (a) người ta gửi tất cả sách cho Daisy, và trong sách ấy có hình. Ở câu (b) người ta chỉ gửi cho Daisy những quyển sách có hình, những quyển khác không có hình và không được gửi cho Daisy.</h4>
<h4>Bỏ Relative Pronoun</h4>
<h4>Đại từ quan hệ có thể được hiểu ngầm nếu đó là túc từ trong loại mệnh đề xác định (defining clause).</h4>
<h4>The book that) I want is on the table.</h4>
<h4>There’s something (that) you don’t know.</h4>
