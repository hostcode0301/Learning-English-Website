	<h3>*THE PRESENT PERFECT TENSE (Thì hiện tại hoàn thành)</h3>

<h4> 1. Công thức:</h4>
<h4>Khẳng định:       S + have / has + V3/-ed</h4>
<h4>Phủ định:           S + haven’t / hasn’t + V3/-ed</h4>
<h4>Nghi vấn:           (Wh-) + have / has + S + V3/-ed …?</h4>
<h4>2. Cách dùng:</h4>
<h4>- Diễn tả một hành động xảy ra trong quá khứ, kéo dài đến hiện tại và có thể tiếp tục trong tương lai.</h4>
<h4>Ex: I have learnt English since 2002.</h4>
<h4>                   She has worked in that company for ten years.</h4>
<h4>- Diễn tả một hành động đã xảy ra trong quá khứ nhưng không có thời gian xác định.</h4>
<h4>Ex: I have read that novel several times.</h4>
<h4>- Diễn tả một hành động vừa mới xảy ra, hay vừa mới hoàn tất.</h4>
<h4>Ex: She has just gone out.</h4>
<h4>3.Dấu hiệu nhận biết:Trong câu thường có các từ:</h4>
<h4>- never: chưa bao giờ</h4>
<h4>- ever: có bao giờ, đã từng</h4>
<h4>- just: vừa mới</h4>
<h4>- since + mốc thời gian (since 2000 / last summer )</h4>
<h4>- for + khoảng thời gian (for ages / two years)</h4>
<h4>- already: đã rồi</h4>
<h4>- how long: bao lâu</h4>
<h4>- before: trước đây</h4>
<h4>- recently, lately: gần đây  </h4>
<h4> - yet: chưa(dùng trong câu phủ định và câu hỏi)</h4>
<h4>- many times, several times: nhiều lần</h4>
<h4>- in the past (ten) years: trong (mười) năm qua</h4>
<h4>- in the last (years): những (năm) gần đây</h4>
<h4>- so far, up to now, up to the present: cho tới bây giờ</h4>
<h4>- this is the first time / the second time: đây là lần đầu / lần thứ hai</h4>
<h4>Ex:</h4>
<h4>- Have you seen any good films recently?</h4>
<h4>-          Up to the present, he has written five children books.</h4>
<h4>-          Have you done your homework yet?</h4>
<h4>-          He has never told a lie.</h4>
<h4>-          I have ever been to Europe.</h4>
<h4>-          We have already had our breakfast.</h4>
