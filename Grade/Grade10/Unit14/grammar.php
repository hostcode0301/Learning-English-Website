    	<h4>+ Khẳng định: S + am/is/are + ....</h4>
        <h4>+ Phủ định:   S + am/is/are + not + ....</h4>
        <h4>+ Câu hỏi:    Am/Is/Are + S + .... ?</h4>
	<h4>- Động từ thường:</h4>
    	<h4>+ Khẳng định: S + V1(s/es) + ....</h4>
        <h4>+ Phủ định:   S + do/does + not + V0 + ....</h4>
        <h4>+ Câu hỏi:    Do/Does + S + V0 + .... ?</h4>
    <h4>- Cách dùng:</h4>
    	<h4>+ Diễn tả một hành động lập đi lập lại trong hiện tại</h4>
        <h4>+ Diễn tả một thói quen hoặc những việc làm thường xuyên ở hiện tại</h4>
        <h4>+ Diễn tả chân lý, sự thật hiển nhiên</h4>
    <h4>- Từ nhận diện:</h4>
    	<h4>Always, usually, sometimes, as a rule, occasionally, normally, often, never,....</h4>
